package ru.job4j.loop;

public class Loop {
    //  Шаблон psvm  - генерация метода main
    public static void main(String[] args) {
        //for через шаблон fori
        for (int i = 0; i <= 10; i++) {
            //Вывод на консоль через шаблон sout
            System.out.println(i);
        }
    }
}
